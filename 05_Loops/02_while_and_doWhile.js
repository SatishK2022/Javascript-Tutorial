// # while Loop

let index = 1
while (index <= 10) {
    // console.log(`Value is ${index}`);
    index += 2
}

let myArray = ["superman", "batman", "spiderman", "ironman", "captain america"]
let arr = 0

while (arr < myArray.length) {
    // console.log(`Value is ${myArray[arr]}`);
    arr++
}

// # do while Loop
let score = 11

do {
    console.log(`Score is ${score}`);
} while (score <= 10);